export function searchHotels(city?: string): Hotel[] {
  if (!city) {
    return hotels;
  }

  const searchTerm = city.toLowerCase();
  return hotels.filter((hotel) =>
    hotel.city.toLowerCase().includes(searchTerm) ||
    hotel.country.toLowerCase().includes(searchTerm)
  );
}
